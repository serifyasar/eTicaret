     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,
            ['name'=>'required',
                'surname'=>'required',
                'tel'=>'required',
                'email'=>'required',
                'adress'=>'required',
                'postcode'=>'required',
                'city'=>'required',
                'country'=>'required'

            ]);


        Auth::user()->address()->create($request->all());

        $cats=Category::all();
        return view('cart.payment',compact('cats'));

    }

